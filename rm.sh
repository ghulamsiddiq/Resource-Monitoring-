#!/bin/bash
#starting resource monitor
echo "***starting Resource monitor***"
for i in {1..5..1}
do
#RAM Usage 
echo "***********-----------------------------------------------------************">>log.txt
echo "RAM Usage">>log.txt
echo "***********-----------------------------------------------------************">>log.txt
free -h >> log.txt
echo "***********-----------------------------------------------------************">>log.txt
#Hard drive Usage
echo "***********-----------------------------------------------------************">>log.txt
echo "HDD Usage">>log.txt
echo "***********-----------------------------------------------------************">>log.txt
du -h >> log.txt
echo "***********-----------------------------------------------------************">>log.txt
echo "HDD free space">>log.txt
echo "***********-----------------------------------------------------************">>log.txt
df -h >> log.txt
echo "***********-----------------------------------------------------************">>log.txt
#CPU Usage
echo "***********-----------------------------------------------------************">>log.txt
echo "CPU Usage">>log.txt
echo "***********-----------------------------------------------------************">>log.txt
w >> log.txt
echo "***********-----------------------------------------------------************">>log.txt
done
cat log.txt
echo "***Ending Resource Monitor***"

r***"